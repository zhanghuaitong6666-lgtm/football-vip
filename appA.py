            selected_option = st.selectbox("👇 请选择追踪维度:", options)
            if selected_option == "🌍 全盘汇总 (当前选中的所有时间+所有联赛)":
                track_df = log_df
            elif selected_option.startswith("⏰ 时间汇总:"):
                track_df = log_df[log_df['时间'] == selected_option.split("时间汇总: ")[1].split(" |")[0]]
